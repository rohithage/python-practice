# Challenge 8: Print the word "Python" 5 times using a while loop

# Initialize counter variable
iteration_count = 1

# Loop while counter is less than or equal to 5
while iteration_count <= 5:
    print("Python")  # Print the word
    iteration_count += 1  # Increment counter 
    
# Completion message
print("Completed printing 'Python' 5 times!")