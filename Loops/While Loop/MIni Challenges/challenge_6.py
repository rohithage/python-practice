# Challenge 6: Print even numbers from 2 to 20 (inclusive)

# Initialize counter at first even number
i = 2

# Loop while i is less than or equal to 20
while i <= 20:
    print(i)      # Print current even number
    i += 2       # Increment by 2 to get next even number
    
# After loop completes
print("All even numbers printed!")