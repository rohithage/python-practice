# Challenge 5: Countdown from 10 to 1

# Initialize counter at starting value 10
i = 10

# Loop while i is greater than or equal to 1
while i >= 1:
    print(i)  # Print current countdown value
    i = i - 1  # Decrement counter by 1
    
# After loop completes, print "Liftoff!"
print("Liftoff!")